package cn.wgn.workflow.entity;

public class UserGroup {
    /**
     * 用户组ID
     */
    private String id;
    /**
     * 用户组名称
     */
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
